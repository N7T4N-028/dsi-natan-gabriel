public class Exemplos_de_uso {

    public static void main(String[] args) {

        // Valores utilizados nos exemplos
        int numero1 = 10;
        int numero2 = 3;

        // Operações matemáticas básicas
        System.out.println(numero1 + numero2); // Soma
        System.out.println(numero1 - numero2); // Subtração
        System.out.println(numero1 * numero2); // Multiplicação
        System.out.println(numero1 / numero2); // Divisão inteira
        System.out.println(numero1 % numero2); // Resto da divisão

        // Exemplo de incremento e decremento
        int valor = 5;

        ++valor;
        System.out.println(valor);

        --valor;
        System.out.println(valor);

        // Divisão inteira
        int a = 10;
        int b = 3;

        System.out.println(a / b);

        // Divisão decimal
        double c = 10.0d;
        double d = 3.0d;

        System.out.println(c / d);

    }
}
